// Aplicacion que cacula el area de un rectangulo
#include <iostream>
using namespace std;

int main()
{
    float area, base = 5, altura = 10;
    area = base * altura;
    cout << "El area del rectangulo con las medidas de: " << base << " de base y " << altura << " de altura es igual a: " << area << endl;
    return 0;
}